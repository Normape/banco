    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String query = "SELECT * FROM cliente WHERE email = '" + email.getText() + "' and cedula = '" + contrasena.getText() + "';";
        try {
            Statement sentencia = bd.conectar().createStatement();
            ResultSet resultado = sentencia.executeQuery(query);

            while (resultado.next()) {
                int id = resultado.getInt("id");
                String cedula = resultado.getString("cedula");
                String nombre = resultado.getString("nombre");
                String direccion = resultado.getString("direccion");
                String email = resultado.getString("email");
                String telefono = resultado.getString("telefono");
                String celular = resultado.getString("celular");
                int saldo = resultado.getInt("saldo");
                // Imprimir los resultados.
                lobby obj= new lobby(id, saldo);
                obj.setVisible(true);
                dispose();
            }
            if(!resultado.first()) {
                JOptionPane.showMessageDialog(null,"Usuario invalido", "Error", JOptionPane.ERROR_MESSAGE);
            }
            sentencia.close();
            bd.conexion.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

