    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DBConexion bd = new DBConexion();
            Statement sentencia = bd.conectar().createStatement();
            
            String query = "INSERT INTO cliente(cedula, " + "nombre, " + "direccion, " + "email, "
                    + "telefono,"  + "celular," + "saldo" + ") VALUES ('"  + cedula.getText() + "', " + "'" + nombre.getText() + "', " +
                    "'" + direccion.getText() + "', " + "'" + email.getText() + "', '" + telefono.getText()
                    + "', '" + celular.getText() + "',"+ Integer.parseInt(saldo.getText()) +");";
            
            if (sentencia.executeUpdate(query) > 0) {
                JOptionPane.showMessageDialog(null,"El registro se insert√≥ exitosamente.");
            } else {
                JOptionPane.showMessageDialog(null,"No se pudo insertar el registro.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            System.out.println(query);
            sentencia.close();
            bd.conexion.close();
        } catch (SQLException ex) {
            Logger.getLogger(crearUsuario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

